package hu;

public class SumOfSubstantiveVoters {
	
	Integer noStamper;
	Integer stamped;
	Integer differenceBetweenVotersAndStamped;
	Integer invalid;
	Integer valid;
	
	public Integer getNoStamper() {
		return noStamper;
	}
	
	public void setNoStamper(Integer noStamper) {
		this.noStamper = noStamper;
	}
	
	public Integer getStamped() {
		return stamped;
	}
	
	public void setStamped(Integer stamped) {
		this.stamped = stamped;
	}
	
	public Integer getDifferenceBetweenVotersAndStamped() {
		return differenceBetweenVotersAndStamped;
	}
	
	public void setDifferenceBetweenVotersAndStamped(Integer differenceBetweenVotersAndStamped) {
		this.differenceBetweenVotersAndStamped = differenceBetweenVotersAndStamped;
	}
	
	public Integer getInvalid() {
		return invalid;
	}
	
	public void setInvalid(Integer invalid) {
		this.invalid = invalid;
	}
	
	public Integer getValid() {
		return valid;
	}
	
	public void setValid(Integer valid) {
		this.valid = valid;
	}	

}
